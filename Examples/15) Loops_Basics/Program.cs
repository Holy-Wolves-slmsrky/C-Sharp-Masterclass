
/*
 * Loops are used to perform a specific task multiple times.
 * This allows for more efficient solutions with less code, rather than writing multiple lines of code to repeat the same task.
 * In C#, there are four main types of loops. Each has a different function and provides various advantages in specific scenarios.
 * Loops execute their code blocks (the code inside the curly braces {}) based on a condition, running one or more times.

 * 1) For Loop:
 * Typically used to repeat a task a specific number of times.
 * The initial value, the termination condition, and the step value are defined.

 * 2) While Loop:
 * The loop continues as long as the condition is true.
 * The condition is checked before the loop starts.

 * 3) Do-While Loop:
 * The loop block is executed first, and then the condition is checked. This means the loop runs at least once.

 * 4) Foreach Loop:
 * Collections (arrays, lists, etc.) are used to process each element sequentially, without using an index.

 * These loops are used in C# based on different needs and help make the code more efficient.

 * Döngüler, belirli bir işlemi birden fazla kez gerçekleştirmek için kullanılır.
 * Böylece aynı işlemi tekrarlamak için birden fazla satır kod yazmak yerine, daha az kod ile daha verimli çözümler elde edilir.
 * C# dilinde dört temel döngü türü bulunur. Her biri farklı işlevlere sahip olup, belirli senaryolarda farklı avantajlar sağlar.
 * Döngüler, kendilerine ait kod bloklarını ({} içinde bulunan kodları) belirli bir koşula göre bir veya daha fazla kez çalıştırır.

 * 1) For Döngüsü:
 * Genellikle belirli bir sayıda tekrarı gerçekleştirmek için kullanılır.
 * Başlangıç değeri, bitiş koşulu ve adım değeri belirlenir.

 * 2) While Döngüsü:
 * Koşul doğru olduğu sürece döngü çalışır.
 * Koşul, döngü başlamadan önce kontrol edilir.

 * 3) Do-While Döngüsü:
 * İlk olarak döngü bloğu çalıştırılır, ardından koşul kontrol edilir. Yani, döngü en az bir kez çalışır.

 * 4) Foreach Döngüsü:
 * Koleksiyonlar (diziler, listeler vb.) içindeki her öğe üzerinde sırayla işlem yapmak için kullanılır ve indeks kullanılmaz.

 * Bu döngüler, C# dilinde farklı ihtiyaçlara göre kullanılır ve kodun verimli olmasını sağlar.
 */
