
/*
 * Code standards are a set of guidelines and conventions that developers follow when writing source code for a project.
 * Some of these are the following:
 * Variables, methods, and classes should be given names that reflect their purpose.
 * Comment lines should be used to explain the code.
 * Code should be readable, maintainable and suitable for teamwork.

 * Kod standartları, geliştiricilerin bir proje için kaynak kodu yazarken izledikleri bir dizi kılavuz ve stildir.
 * Bunlardan bazıları şunlardır:
 * Değişkenleri, yöntemleri ve sınıfları adlandırırken, onlara amaçlarını ifade eden bir isim verilmelidir.
 * Kodu açıklamak için yorum satırları kullanılmalıdır.
 * Kod okunabilir, bakımı yapılabilir ve ekip çalışmasına uygun olmalıdır.
 */

/*
 * Naming conventions:
 * 1) Camel Case: The first word starts with a lowercase letter, and each subsequent word starts with an uppercase letter. It is used for naming variables.
 * 2) Pascal Case: The first letter of each word is capitalized. It is used for naming classes and methods.
 * 3) Snake Case: All words are separated by underscores and the letters are lowercase. It is commonly used for file names or constants.

 * Adlandırma kuralları:
 * 1) Camel Case: İlk kelime küçük harf ile başlar ve sonraki her kelime büyük harf ile başlar. Değişken adlandırmasında kullanılır.
 * 2) Pascal Case: Tüm kelimeler büyük harf ile başlar. Sınıf ve metod adlandırmasında kullanılır.
 * 3) Snake Case: Tüm kelimeler küçük harf ile başlar ve '_' karakteri ile ayrılır. Genellikle dosya adlarında veya bazı sabitlerde kullanılır.

 * Example - Örnek:
 * Camel Case: myVariableName
 * Pascal Case: MyClassName, MyMethodName
 * Snake Case: my_variable_name
 */

/*
 * Comments can be used for:
 * 1) Adding explanations about what the code does.
 * 2) Providing information about why the code is written in a certain way.
 * 3) Leaving notes for future improvements or changes.

 * Yorum satırları şunlar için kullanılabilir:
 * 1) Kodun ne yaptığı hakkında açıklamalar eklemek.
 * 2) Kodun neden böyle yazıldığına dair bilgiler sağlamak.
 * 3) Gelecekte yapılacak iyileştirmeler veya değişiklikler için notlar bırakmak.
 */

